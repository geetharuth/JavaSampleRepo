package Javaoperators;

public class Auto_inc_dec {

	public static void main(String[] args) {
		// Auto increment and decrement 
		int x=200;
		x++;
		System.out.println("increment x with  one time------"+x);
		int y=300;
		y+=10;
		System.out.println("increment x with more than ten times------"+y);
		int a =400;
		a--;
		System.out.println("decrement a with  one time------"+a);
		int b=300;
		b-=50;
		System.out.println("decrement b with more than fifty times-----"+b);

	}

}
