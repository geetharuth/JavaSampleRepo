package Javaoperators;

public class Comparisoinoperators {

	public static void main(String[] args) {
		// Comparission operators
		int x=500, y=300, z=500;
		
		System.out.println(x==z);//equals to
		System.out.println(x==y);//
		System.out.println(x!=y);//no equals
		System.out.println(x!=z);//
		System.out.println(x>z);//greater
		System.out.println(x<z);//less
		System.out.println(x>=z);//greater than equals
		System.out.println(x<=z);//lesser than equals



	}
}
