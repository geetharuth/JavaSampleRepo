package Javaloopings;

public class DoWhileloop {

	public static void main(String[] args) {

//print odd numbers 1-20
		int x1=1;
		do 
		{
			System.out.println(x1);
			x1 +=2;
		}
		while (x1<=20);
		//another example
		int x=5;
		do
		{
			System.out.println(x);
			x++;
		}
while(x<=20);
	}

}
