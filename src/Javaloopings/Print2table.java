public class Print2table {

	public static void main(String[] args) {
		// print 2 table
		int number =2;
		for(int i=1;i<=10;i++)
		{
			System.out.println(number +"x"+i+"="+(number *i));
		}
//print 3 table 
		int number1 =3;
		for(int i=1;i<=10;i++)
		{
			System.out.println(number1+"x"+i+"="+(number1*i));
		}

	}

}
